const os = require('os');

// OS Platform
console.log(os.platform());

// OS arch
console.log(os.arch());

// CPU core
console.log(os.cpus());

// Free memory
console.log(os.freemem());

// Total memory
console.log(os.totalmem());

// Home dir
console.log(os.homedir());

// Uptime 
console.log(os.uptime());